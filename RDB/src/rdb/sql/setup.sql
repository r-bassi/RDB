CREATE TABLE League (division VARCHAR(64), tvDeal REAL NOT NULL, sponsorship REAL NOT NULL, salaryCap REAL NOT NULL, commissioner VARCHAR(64), PRIMARY KEY (division));
CREATE TABLE Team (city VARCHAR(64), name VARCHAR(64), homeCapacity INTEGER NOT NULL, payroll REAL NOT NULL, merchandise REAL NOT NULL, ticketPrice REAL NOT NULL, division VARCHAR(64), gmId VARCHAR(64) UNIQUE NOT NULL, PRIMARY KEY (city, name), FOREIGN KEY (division) REFERENCES League ON DELETE SET NULL);
CREATE TABLE GeneralManager (id VARCHAR(64), passcode VARCHAR(64), name VARCHAR(64) NOT NULL, experience INTEGER NOT NULL, teamCity VARCHAR(64) UNIQUE NOT NULL, teamName VARCHAR(64) UNIQUE NOT NULL, PRIMARY KEY (id), FOREIGN KEY (teamCity, teamName) REFERENCES Team ON DELETE CASCADE);
CREATE TABLE Contract (id INTEGER, salary REAL NOT NULL, duration INTEGER NOT NULL, gmId VARCHAR(64) NOT NULL, PRIMARY KEY (id), FOREIGN KEY (gmId) REFERENCES GeneralManager ON DELETE CASCADE);
CREATE TABLE Agent (id INTEGER, name VARCHAR(64) NOT NULL, agentFee REAL NOT NULL, contractId INTEGER UNIQUE NOT NULL, PRIMARY KEY (id), FOREIGN KEY (contractId) REFERENCES Contract ON DELETE CASCADE);
CREATE TABLE Coach (id INTEGER, name VARCHAR(64) NOT NULL, rating INTEGER NOT NULL, agentId INTEGER NOT NULL, PRIMARY KEY (id), FOREIGN KEY (agentId) REFERENCES Agent ON DELETE CASCADE);
CREATE TABLE Player (id VARCHAR(64), passcode VARCHAR(64), name VARCHAR(64) NOT NULL, age INTEGER, height REAL, weight REAL, winshare REAL NOT NULL, draftYear INTEGER NOT NULL, coachId INTEGER NOT NULL, agentId INTEGER NOT NULL, PRIMARY KEY (id), FOREIGN KEY (coachId) REFERENCES Agent ON DELETE CASCADE,  FOREIGN KEY (agentId) REFERENCES Coach ON DELETE CASCADE);
CREATE TABLE Plays (userId VARCHAR(64), teamCity VARCHAR(64), teamName VARCHAR(64), PRIMARY KEY (userId, teamCity, teamName), FOREIGN KEY (userId) REFERENCES Player ON DELETE CASCADE,  FOREIGN KEY (teamCity, teamName) REFERENCES Team ON DELETE CASCADE);
CREATE TABLE Trade (id INTEGER, playerInId VARCHAR(64) NOT NULL, playerOutId VARCHAR(64) NOT NULL, winshareDiff REAL NOT NULL, salaryDiff REAL NOT NULL, gmId VARCHAR(64) NOT NULL, division VARCHAR(64) NOT NULL, PRIMARY KEY (id), FOREIGN KEY (playerInId) REFERENCES Player ON DELETE CASCADE,  FOREIGN KEY (playerOutId) REFERENCES Player ON DELETE CASCADE,  FOREIGN KEY (gmId) REFERENCES GeneralManager ON DELETE CASCADE);
